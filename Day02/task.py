#Subscripting : Extracting a part of a string
print("Hello"[0]) # H

#String - Concatenation
print("123" + "345") # 123345

#Integer - Whole number
print(123 + 345) # 468
 
#Large Integer
print(123_456_789) # 468
 
#Float - Decimal number
print(3.14159) # 3.14159

#Boolean - True or False
print(True)
print(False)

#string literals
#f-string : String formatting 
score = 0
print(f"Your score is {score}") # Your score is 0
height = 1.8
is_winning = True

print(f"Your score is {score}, your height is {height}, you are winning is {is_winning}") # Your score is 0, your height is 1.8, you are winning is True
